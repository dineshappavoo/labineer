package vo;

public class ProductsVO implements java.io.Serializable{

	
	 private String Productid=null;
	 private String product_name=null;
	 private String product_category=null;
	 private String product_description=null;
	 private String product_specification=null;
	 private String product_standard=null;
	 private String product_features=null;
	 private String product_brand=null;
	 private String product_availability=null;
	 public String getProductid() {
		return Productid;
	}
	public void setProductid(String productid) {
		Productid = productid;
	}
	public String getProduct_name() {
		return product_name;
	}
	public void setProduct_name(String product_name) {
		this.product_name = product_name;
	}
	public String getProduct_category() {
		return product_category;
	}
	public void setProduct_category(String product_category) {
		this.product_category = product_category;
	}
	public String getProduct_description() {
		return product_description;
	}
	public void setProduct_description(String product_description) {
		this.product_description = product_description;
	}
	public String getProduct_specification() {
		return product_specification;
	}
	public void setProduct_specification(String product_specification) {
		this.product_specification = product_specification;
	}
	public String getProduct_standard() {
		return product_standard;
	}
	public void setProduct_standard(String product_standard) {
		this.product_standard = product_standard;
	}
	public String getProduct_features() {
		return product_features;
	}
	public void setProduct_features(String product_features) {
		this.product_features = product_features;
	}
	public String getProduct_brand() {
		return product_brand;
	}
	public void setProduct_brand(String product_brand) {
		this.product_brand = product_brand;
	}
	public String getProduct_availability() {
		return product_availability;
	}
	public void setProduct_availability(String product_availability) {
		this.product_availability = product_availability;
	}
	
	
}
